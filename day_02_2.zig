fn common_chars(ids: [] const [] const u8) void {
    for (ids) |id, i| {
        outer: for (ids[i + 1..]) |id2, j| {
            if (i != j) {
                debug.assert(id.len == id2.len);
                var distance: u32 = 0;
                for (id) |char, k| {
                    if (char != id2[k]) {
                        distance += 1;
                    }
                    if (distance > 1) {
                        continue :outer;
                    }
                }
                if (distance == 1) {
                    //debug.warn("{}\n", id);
                    //debug.warn("{}\n", id2);
                    for (id) |char, x| {
                        if (char == id2[x]) {
                            debug.warn("{c}", char);
                        }
                    }
                    debug.warn("\n");

                    break;
                }
            }
        }
    }
}

